package model;
/**
 * This interface is used to compare two different objects.
 * @author Hung Vu
 * @version 27 10 2019
 * @param <T> generic object for comparator
 */
public interface Comparator<T> {
    /**
     * This method will compare two different objects.
     * @param theFirst the first object
     * @param theSecond the second object
     * @return an integer showing order of two object
     */
    int compare(T theFirst, T theSecond);
}
